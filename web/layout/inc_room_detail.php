    function initRoomDetailJS() {
        try {
            const viewMoreButton = document.querySelector('.view-more-button');
            const hiddenOptions = document.querySelectorAll('.room-option.hidden');

            viewMoreButton.addEventListener('click', () => {
                hiddenOptions.forEach(option => {
                    option.classList.toggle('hidden');
                });
                viewMoreButton.textContent = hiddenOptions[0].classList.contains('hidden')
                    ? 'Xem thêm 2 lựa chọn'
                    : 'Thu gọn';
            });

        } catch (error) {
            console.error('Error in overview_detail.js:', error);
        }
    }

    // Kiểm tra jQuery đã load chưa
    if (window.jQuery) {
        $(document).ready(initRoomDetailJS);
    } else {
        const waitForJQuery = setInterval(() => {
            if (window.jQuery) {
                clearInterval(waitForJQuery);
                $(document).ready(initRoomDetailJS);
            }
        }, 100);
    }
